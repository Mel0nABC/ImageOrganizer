package com.example.weblogin.otherClasses;

public class ImageProperties {

    private int height, width, transparencia, type, size;
    private String rutaAbsoluta;
    
    public ImageProperties() {
    }


    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getTransparencia() {
        return transparencia;
    }

    public void setTransparencia(int transparencia) {
        this.transparencia = transparencia;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }


    public String getRutaAbsoluta() {
        return rutaAbsoluta;
    }


    public void setRutaAbsoluta(String rutaAbsoluta) {
        this.rutaAbsoluta = rutaAbsoluta;
    }


    public int getSize() {
        return size;
    }


    public void setSize(int size) {
        this.size = size;
    }

}
